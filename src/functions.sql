CREATE FUNCTION IF NOT EXISTS existciclo(cl VARCHAR(2))
	RETURNS BOOLEAN DETERMINISTIC
	BEGIN 
	IF cl='1S' THEN
		RETURN 1;
	ELSEIF cl='2S' THEN
		RETURN 1;
	ELSEIF cl='VJ' THEN
		RETURN 1;
	ELSEIF cl='VD' THEN
		RETURN 1;
	ELSE
		RETURN 0;
	END IF;
END $$
DELIMITER ;

DELIMITER $$
CREATE FUNCTION IF NOT EXISTS isFecha(dt VARCHAR(12))
	RETURNS BOOLEAN DETERMINISTIC
	BEGIN
	RETURN IF (dt REGEXP '^[0-9]{1,2}:[0-9]{2}-[0-9]{1,2}:[0-9]{2}$',true,false)
END $$
DELIMITER ;

DELIMITER $$
CREATE FUNCTION IF NOT EXISTS isDia(dia INT)
	RETURNS BOOLEAN DETERMINISTIC
	BEGIN
	IF dia>0 AND dia<8 THEN
		RETURN 1;
	ELSE
		RETURN 0;
	END IF;
END $$
DELIMITER ;