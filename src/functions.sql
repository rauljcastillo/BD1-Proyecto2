DELIMITER $$
CREATE FUNCTION IF NOT EXISTS validemail(correo VARCHAR(50))
	RETURNS BOOLEAN DETERMINISTIC
	BEGIN 
    RETURN IF (correo REGEXP '^[a-zA-Z0-9]+((\.[a-zA-Z0-9]+)|(\_[a-zA-Z0-9]+))?@[a-zA-Z]+(\.[a-zA-Z]+)+$',true,false);
    
END $$
DELIMITER ;

DELIMITER $$
CREATE FUNCTION IF NOT EXISTS sololetras(str VARCHAR(35))
	RETURNS BOOLEAN DETERMINISTIC
	BEGIN
	RETURN IF (str REGEXP '^[a-zA-Zaáéíóú]*$',true,false);
END $$
DELIMITER ;

DELIMITER $$
CREATE FUNCTION IF NOT EXISTS esPositivo(num INT)
	RETURN BOOLEAN DETERMINISTIC
	BEGIN
	IF num>=0 THEN
		RETURN 1;
	ELSE
		RETURN 0;
	END IF;
END $$
DELIMITER ;

